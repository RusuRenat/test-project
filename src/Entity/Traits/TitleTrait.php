<?php

namespace App\Entity\Traits;

use Doctrine\ORM\Mapping as ORM;

trait TitleTrait
{

    /**
     * @var string|null
     *
     * @ORM\Column(name="title", type="string", length=255, nullable=true)
     */
    private $title;

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(?string $title): void
    {
        $this->title = $title;
    }


}